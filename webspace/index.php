  <h1>Apertium: an open-source shallow-transfer machine translation
  engine and toolbox</h1>

  <h2>What is Apertium and who is developing it</h2>

  <p><strong>Apertium</strong> is one of the two open-source
  machine translation engines being developed inside project
  <a href="http://www.opentrad.org">OpenTrad</a>; Apertium is
  designed to translate between related languages (see
  Documentation below). The OpenTrad consortium is led by <a href=
  "http://www.eleka.net">Eleka Ingeniaritza Linguistikoa</a>.</p>

  <p>The project ("Open Source Machine Translation for the Romance
  languages of Spain") is funded by the <a href=
  "http://www.min.es">Ministry of Industry, Tourism and
  Commerce</a> of Spain trough <a href=
  "http://www.mityc.es/profit/">PROFIT</a> grant number
  FIT340101-2004-0003.</p>

  <div class="c1">
    <img src=
    "http://www2.mityc.es/MitycCMS/images/LogoMinisterio.jpg" alt=
    "Ministerio de Industria, Turismo y Comercio" />
  </div>

  <p>The Apertium architecture is being developed by the <a href=
  "http://transducens.dlsi.ua.es">Transducens</a> research group at
  the <a href="http://www.dlsi.ua.es">Departament de Llenguatges i
  Sistemes Informàtics</a> of the <a href=
  "http://www.ua.es/">Universitat d'Alacant</a>. Linguistic data
  for Apertium are being developed by Tranducens (Spanish-Catalan),
  and the <a href="http://webs.uvigo.es/sli/">Seminario de
  Lingüística Informàtica</a> of the <a href=
  "http://www.uvigo.es/indice/index.gl.htm">Universidade de
  Vigo</a> (Spanish-Galician).</p>

  <h2>Software</h2>

  <p>Apertium (packages <code>lttoolbox</code> and
  <code>apertium</code>) as well as the corresponding language-pair
  data will be released through SourceForge page <a href=
  "http://sf.net/projects/apertium/">sf.net/projects/apertium/</a>.
  The expected date of release for version 0.8 and linguistic data
  for Spanish-Catalan is July 29, 2005.</p>

  <h2>Test drive</h2>

  <p>Before installing Apertium, you will be able to evaluate it
  online. The Spanish-Catalan system will be made available on July
  29, 2005 at <a href=
  "http://xixona.dlsi.ua.es/prototype">xixona.dlsi.ua.es/prototype/</a>.</p>

